#Intializing list, tuple, set and dictionary with values
my_list = ["January", "February", "March", "April", "May", "June"]

my_tuple = ("January", "February", "March", "April", "May", "June")

my_set = {"January", "February", "March", "April", "May", "June"}

my_dict = {"January" : 1,  "February" : 2, "March" : 3, "April" : 4, "May" : 5, "June" : 6}



#Printing each collection type
print("My list:  ", type(my_list))
for i in my_list:
    print(i)

print("\n")

print("My tuple:  ", type(my_tuple))
for j in my_tuple:
    print(j)

print("\n")

print("My set:  ", type(my_set))
for k in my_set:
    print(k)

print("\n")

print("My dict:  ", type(my_dict))
for l in my_dict:
    print(l)

